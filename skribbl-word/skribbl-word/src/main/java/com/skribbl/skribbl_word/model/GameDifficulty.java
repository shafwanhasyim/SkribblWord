package com.skribbl.skribbl_word.model;

/**
 * Enum untuk merepresentasikan tingkat kesulitan kata dalam game
 * Digunakan untuk mengkategorikan kata berdasarkan kompleksitas
 */
public enum GameDifficulty {
    EASY,    // Kata dengan tingkat kesulitan rendah
    MEDIUM,  // Kata dengan tingkat kesulitan menengah
    HARD,    // Kata dengan tingkat kesulitan tinggi
    RANDOM   // Kata dengan tingkat kesulitan acak
}
